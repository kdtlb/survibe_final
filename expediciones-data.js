function getExpedicionById(id) {
  return EXPEDICIONES.find(e => e.id === id) || null;
}

function getRelatedExpediciones(excludeId, n = 3) {
  const exp = EXPEDICIONES.find(e => e.id === excludeId);
  if (exp && Array.isArray(exp.relacionadas) && exp.relacionadas.length) {
    return exp.relacionadas.map(getExpedicionById).filter(Boolean).slice(0, n);
  }
  const pool = EXPEDICIONES.filter(e => e.id !== excludeId);
  const out = [];
  const copy = pool.slice();
  while (out.length < n && copy.length) {
    out.push(copy.splice(Math.floor(Math.random() * copy.length), 1)[0]);
  }
  return out;
}
